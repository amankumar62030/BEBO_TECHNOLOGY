# 1.	Define a variable name with your name as its value.
# 2.	Print the variable using three different methods:
# •	Using the print() function directly.
# •	Using an f-string.
# •	Using .format() method.
# Example Output:
# Using print(): My name is John
# Using f-string: My name is John
