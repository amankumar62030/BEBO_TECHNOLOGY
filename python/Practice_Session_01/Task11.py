# 1.	Define two integers, a and b, with values of your choice.
# 2.	Perform the following bitwise operations and print the results:
# •	AND (&)
# •	OR (|)
# •	XOR (^)
# •	NOT (~)
# •	Left Shift (<<)
# •	Right Shift (>>)

a = int(input("Enter the first number:"))
b = int(input("Enter the second number:"))

print(a & b)
print(a | b)
print(~a)
print(a<<2)
print(b>>2)

