# 2.	Explain the differences between positional and keyword arguments.


# 1. Positional Arguments
# These are arguments passed to a function in the order in which they are defined in the function signature.
#
# def greet(first_name, last_name):
#     print(f"Hello, {first_name} {last_name}!")
#
# # Using positional arguments
# greet("John", "Doe")  # Output: Hello, John Doe!
#
#
#
# 2. Keyword Arguments
# These are arguments passed to a function using parameter names explicitly.
# def greet(first_name, last_name):
#     print(f"Hello, {first_name} {last_name}!")
#
# # Using keyword arguments
# greet(last_name="Smith", first_name="Alice")  # Output: Hello, Alice Smith!
